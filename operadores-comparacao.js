const numero1 = 50;
const numero2 = 60;
const condicao = numero1 != numero2;

if (condicao) {
    console.log(`A condição é verdadeira`);
}
else {
    console.log(`A condição é falsa`);
}

// == -> igual
// != -> não é igual
// > -> Maior que 
// < -> Menor que 
// >= -> Maior OU igual
// <= -> Menor OU igual
// === -> Estritamente igual
// !== -> não estritamente igual